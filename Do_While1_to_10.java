package Akki;
public class Do_While1_to_10 
{

	public static void main(String[] args)
	{
		/*Write a Program to Display 1 to 10 Numbers using Do_While Loop*/
		System.out.println("1 to 10 Numbers using Do_While Loop");
		int i=1;
		do
		{
			System.out.println(i);
			i++;
			
		}
		while (i<=10);
		
	}

}
